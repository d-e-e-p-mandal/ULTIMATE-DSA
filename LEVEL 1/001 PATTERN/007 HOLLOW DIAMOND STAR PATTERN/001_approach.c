#include<stdio.h>
void hollowDiamondPattern(int n) {
    for(int i=0; i<n; i++) {
        for(int j=0; j<n-i; j++) {
            printf("*");
        }
        for(int j=0; j<i; j++) {
            printf("  ");
        }
        for(int j=0; j<n-i; j++) {
            printf("*");
        }
        printf("\n");
    }

    for(int i=1; i<n; i++) {
        for(int j=0; j<=i; j++) {
            printf("*");
        }
        for(int j=0; j<n-i-1; j++) {
            printf("  ");
        }
        for(int j=0; j<=i; j++) {
            printf("*");
        }
        printf("\n");
    }
}
int main() {

    int n;
    printf("Enter N : ");
    scanf("%d",&n);

    hollowDiamondPattern(n);
    return 0;
}

