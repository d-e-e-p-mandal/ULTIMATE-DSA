#include<stdio.h>
void hollowDiamondPattern(int n) {
    for(int i=0; i<2*n-1; i++) {
        int star = (i < n) ? n-i : i-n+2;
        int space = (i < n) ? i : (2*n)-i-2;
        
        for(int j=0; j<star; j++) {
            printf("*");
        }
        for(int j=0; j<space; j++) {
            printf("  ");
        }
        for(int j=0; j<star; j++) {
            printf("*");
        }
        printf("\n");
    }
}
int main() {

    int n;
    printf("Enter N : ");
    scanf("%d",&n);

    hollowDiamondPattern(n);
    return 0;
}

